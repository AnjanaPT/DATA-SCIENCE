'''NumPy program to create a 5*5 zero matrix with elements on the main diagonal equal to 1,2,3,4 and 5'''
import numpy as np
a = np.diag([1, 2, 3, 4, 5])
print("\n----------------------------------------------------------------\n5*5 Zero Matrix With 1,2,3,4,and 5 as Main Diagonal Elements")
print("----------------------------------------------------------------\n",a,"\n----------------------------------------------------------------\n")
